type Field struct {
	Name string
	Type types.Type
	Tag  string
}

func generate(destPkg string, structName *types.TypeName) error {
	// Iterate over struct fields
	fields := getFields(structName)
	if len(fields) == 0 {
		return nil
	}

	// Start a new file in destination package
	f := jen.NewFile(destPkg)

	// Add a package comment, so IDEs detect files as generated
	// TODO version configurable
	f.PackageComment("Code generated by badctl v" + version.Version + ", DO NOT EDIT.")

	for _, field := range fields {
		log.Println(field.Name)
		addConditionForField(
			f,
			destPkg,
			structName,
			field,
		)
	}

	// Write generated file
	return f.Save(strings.ToLower(structName.Name()) + "_conditions.go")
}

func getFields(structName *types.TypeName) []Field {
	fields := []Field{}

	// Generate only when underlying type is a struct
	// (ignore const, var, func, etc.)
	structType := getBadORMModelStruct(structName)
	if structType != nil {
		// Iterate over struct fields
		for i := 0; i < structType.NumFields(); i++ {
			field := structType.Field(i)
			fields = append(fields, Field{
				Name: field.Name(),
				Type: field.Type(),
				// TODO verificar que pasa si hay menos tags, osea que algunos atributos no tienen tags
				Tag: structType.Tag(i),
			})
		}
	}

	return fields
}

func getBadORMModelStruct(structName *types.TypeName) *types.Struct {
	structType, ok := structName.Type().Underlying().(*types.Struct)
	if !ok || !isBadORMModel(structType) {
		return nil
	}

	return structType
}

func isBadORMModel(structType *types.Struct) bool {
	for i := 0; i < structType.NumFields(); i++ {
		field := structType.Field(i)

		if field.Embedded() && pie.Contains(badORMModels, field.Name()) {
			return true
		}
	}

	return false
}

var basicParam = jen.Id("v")

func addConditionForField(f *jen.File, destPkg string, structName *types.TypeName, field Field) {
	conditions := generateConditionForField(
		destPkg,
		structName, field,
		basicParam.Clone(),
	)

	for _, condition := range conditions {
		f.Add(condition)
	}
}

func generateConditionForField(destPkg string, structName *types.TypeName, field Field, param *jen.Statement) []jen.Code {
	switch fieldTypeTyped := field.Type.(type) {
	case *types.Basic:
		return []jen.Code{generateWhereCondition(
			destPkg,
			structName,
			field.Name,
			typeKindToJenStatement[fieldTypeTyped.Kind()](param),
		)}
	case *types.Named:
		fieldTypeName := fieldTypeTyped.Obj()
		if pie.Contains(badORMModels, fieldTypeName.Name()) {
			return generateBaseModelConditions(
				destPkg,
				structName,
				fieldTypeTyped,
			)
			// TODO UUID tambien tiene que generar el join si otro no lo genera
		} else if fieldTypeName.Name() == "UUID" || fieldTypeTyped.String() == "time.Time" {
			return []jen.Code{
				generateWhereCondition(
					destPkg,
					structName,
					field.Name,
					param.Clone().Qual(
						getRelativePackagePath(fieldTypeName.Pkg(), destPkg),
						fieldTypeName.Name(),
					),
				),
			}
			// TODO DeletedAt fieldTypeTyped.String() == "gorm.io/gorm.DeletedAt"
		} else if getBadORMModelStruct(fieldTypeName) != nil {
			// TODO que pasa si esta en otro package? se importa solo?
			joinCondition := generateJoinCondition(
				destPkg,
				structName,
				field.Name, fieldTypeName,
			)

			fields := getFields(structName)
			if !pie.Any(fields, func(otherField Field) bool {
				// TODO posible override?
				return otherField.Name == field.Name+"ID"
			}) {
				return []jen.Code{
					joinCondition,
					generateJoinCondition(
						destPkg,
						fieldTypeName,
						// TODO Override Foreign Key
						structName.Name(), structName,
					),
				}
			}

			return []jen.Code{joinCondition}
		}
	case *types.Pointer:
		return generateConditionForField(
			destPkg,
			structName,
			Field{
				Name: field.Name,
				Type: fieldTypeTyped.Elem(),
				Tag:  field.Tag,
			},
			param.Clone().Op("*"),
		)
	case *types.Slice:
		elemType := fieldTypeTyped.Elem()
		elemTypeTyped, ok := elemType.(*types.Named)
		if !ok {
			// TODO ver que pasa si quiero guardar una list de strings o algo asi
			return []jen.Code{}
		}

		elemTypeName := elemTypeTyped.Obj()
		if getBadORMModelStruct(elemTypeName) != nil {
			log.Println(elemTypeName.Name())
			return []jen.Code{
				generateJoinCondition(
					destPkg,
					elemTypeName,
					// TODO Override Foreign Key
					structName.Name(), structName,
				),
			}
		}
	default:
		log.Printf("struct field type not hanled: %T", fieldTypeTyped)
	}

	// TODO ver este error
	return []jen.Code{}
}

